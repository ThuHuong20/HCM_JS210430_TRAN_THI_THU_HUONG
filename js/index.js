let currentPage = 1;
let totalPage = 6;
let start;
let end;
// danh sach san pham hien tai
let nowProductList = [];
// tinh index dau va index cuoi de lay dah sach san pham tuong ung
function currentPageNow(indexPageNow) {
    start = (indexPageNow - 1) * totalPage;
    end = indexPageNow * totalPage;
}
   // hien thi page
function showAllListPage() {
    let numbersPage = Math.ceil(nowProductList.length / totalPage);
    let result = "";
    for (let i = 0; i < numbersPage; i++) {
        result +=
            `
       <li style="cursor: pointer; user-select: none;" onclick="pageNow(${i})" class="item-page"> ${i + 1} </li>
       `
    }
    document.getElementById("listPage").innerHTML = result;
}
    // xet mau cho page hien tai
function activeNowPage() {
    const itemPageList = document.querySelectorAll('.item-page');
    for (let i in itemPageList) {
        if (i == "entries") {
            break;
        }
        if (Number(i) + 1 == currentPage) {
            itemPageList[i].classList.add('active-page');
        } else {
            itemPageList[i].classList.remove('active-page');
        }
    }
}
// function next page   
function increasePage() {
    currentPage++;
    let numbersPage = Math.ceil(nowProductList.length / totalPage);
    if (currentPage > numbersPage) {
        currentPage = numbersPage;
        return
    }
    // xac dinh star va end
    currentPageNow(currentPage);
    // hien thi san pham
    renderListProducts(nowProductList);
    // hien thi page
    showAllListPage();
    // xet mau cho page hien tai
    activeNowPage();
}
function decreasePage() {
    if (currentPage == 1) {
        return
    }
    currentPage--;
    currentPageNow(currentPage);
    renderListProducts(nowProductList);
    showAllListPage();
    activeNowPage();
}
//function click từng page
function pageNow(pageNow) {
    currentPage = pageNow + 1;
    currentPageNow(currentPage);
    renderListProducts(nowProductList);
    showAllListPage();
    activeNowPage();
}
// function hien thi san pham
function renderListProducts(productList) {
    let result = ``;
    for (let i in productList) {
        if (i >= start && i < end) {
            result +=
                `
            <div class="productItem" onclick="renderProductItem('${productList[i].id}')">
                <div class="product-image">
                    <img src="${productList[i].image}" alt="">
                    <p class="buyNow">Mua ngay</p>
                </div>
                <div class="product-info">
                    <p>${productList[i].name}</p>
                    <span>${VND.format(productList[i].price)}</span>
                </div>
            </div>
         `
                ;
        }
    }
    document.querySelector(".content-container").innerHTML =
        `       ${result}
            <div class="list-page">
                <span onclick="decreasePage()" style="user-select: none" class="material-symbols-outlined">chevron_left</span>
                <ul id="listPage"></ul>
                <span onclick="increasePage()" style="user-select: none" class="material-symbols-outlined">chevron_right</span>
            </div>
    `;
}
