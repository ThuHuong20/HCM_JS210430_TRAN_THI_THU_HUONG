let currentIndex = 0;
function initSlideBar() {
    const slideBar = document.querySelector('.slideBar__content');
    const circleGroups = slideBar.querySelector('.circle__groups');
    const itemDefault = slideBar.querySelector('.default');
    const itemNext = slideBar.querySelector('.next');
    // ve hinh tron
    let circleGroupsContent = '';
    for (let i in slideItems) {

        if (i == 0) {
            circleGroupsContent += `
                <div class="circle active"></div>
            `
            continue;
        }

        circleGroupsContent += `
            <div class="circle"></div>
        `
    }
    circleGroups.innerHTML = circleGroupsContent;
    let circleList = circleGroups.querySelectorAll('.circle');
    return [circleList, slideBar, itemDefault, itemNext]
}
function sleep(ms) {
    return new Promise(resolve => setTimeout(resolve, ms));
}
async function actionSlideBar() {
    [circleList, slideBar, itemDefault, itemNext] = initSlideBar();
    itemDefault.innerText = slideItems[currentIndex].content; // 0
    itemNext.innerText = slideItems[currentIndex + 1].content; // 1
    currentIndex = 1;
    while (true) {
        setTimeout(async () => {
            let clonedElement = itemNext.cloneNode(true);
            slideBar.appendChild(clonedElement);
            await sleep(800);

            clonedElement.classList.add('active'); // run

            setTimeout(() => {
                itemDefault.innerText = clonedElement.innerText;
                clonedElement.remove();
            }, 1000)

        }, 1000)
        await sleep(2000);
        for (let i in circleList) {

            if (i == 'entries') {
                break
            }
            if (i == currentIndex) {
                circleList[i].classList.add('active');
            } else {
                circleList[i].classList.remove('active');
            }
        }
        currentIndex++;
        if (currentIndex > slideItems.length - 1) {
            currentIndex = 0;
        }
        itemNext.innerText = slideItems[currentIndex].content;
    }
}
actionSlideBar();

