function catalogProduct(type) {
    let listProducts = JSON.parse(localStorage.getItem("listProducts"));
    let listProductFilter = [];
    for (let i = 0; i < listProducts.length; i++) {
        if (listProducts[i].type == type) {
            listProductFilter.push(listProducts[i]);
        }
    }
    nowProductList = listProductFilter;
    document.querySelector(".nagivation").style.display = "none";
    document.querySelector(".sellingProduct").style.display = "none";
    document.querySelector(".slideBar").style.display = "none";
    currentPage = 1
    currentPageNow(currentPage);
    renderListProducts(nowProductList);
    showAllListPage();
    activeNowPage();
}
