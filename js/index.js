// function them vao gio hang
function addToCart(idProduct) {
    let checkLogin = localStorage.getItem("checkLogin");
    if (checkLogin == null) {
        alert("Chưa đăng nhập, không thể mua hàng!")
        return;
    }
    let listProducts = JSON.parse(localStorage.getItem("listProducts"));
    let listUsers = JSON.parse(localStorage.getItem("listUsers"));
    for (let i = 0; i < listUsers.length; i++) {
        if (listUsers[i].idUser == checkLogin) {
            let cartUser = listUsers[i].cartUser;
            for (let h = 0; h < cartUser.length; h++) {
                if (cartUser[h].id == idProduct) {
                    cartUser[h].quantity += 1;
                    localStorage.setItem("listUsers", JSON.stringify(listUsers));
                    showTotalCartProduct();
                    return;
                }
            }
            let seeMoreProduct;
            let seeMore = JSON.parse(localStorage.getItem("seeMore"));
            seeMoreProduct = seeMore.find((item) => {
                return item.id == idProduct
            })
            if (seeMoreProduct) {
                let cartUser = {
                    id: idProduct,
                    quantity: 1,
                    name: seeMoreProduct.name,
                    price: seeMoreProduct.price,
                    image: seeMoreProduct.image,
                }
                listUsers[i].cartUser.push(cartUser);
                localStorage.setItem("listUsers", JSON.stringify(listUsers));
                showTotalCartProduct();
                return;
            }
            for (let j = 0; j < listProducts.length; j++) {
                if (listProducts[j].id == idProduct) {
                    listUsers[i].cartUser.push(listProducts[j]);
                    localStorage.setItem("listUsers", JSON.stringify(listUsers));
                    showTotalCartProduct();
                    return;
                }
            }
        }
    }
}
